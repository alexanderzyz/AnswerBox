clc
clear
clf;
dt=0.05;
vp=200;
vd=500;
p0=[0,0,0];
p2=[5000,500,800];
xd=p0(1);
yd=p0(2);
zd=p0(3);
xp=p2(1);
yp=p2(2);
zp=p2(3);
times=0;
thr=10;
dis=sqrt(abs(xd-xp)^2+abs(yd-yp)^2+abs(zd-zp)^2);
theta=0;
A=100;
while(dis>thr && times<300)
    theta=times*dt*pi;
    cos_x=(xp-xd)/dis;
    cos_y=(yp-yd)/dis;
    cos_z=(zp-zd)/dis;
    times=times+1;
    xp1=p2(1)-vp*dt*times;
    yp1=p2(2)+A*sin(theta);
    zp1=p2(3)+A*(1-cos(theta))+A*times/50;
    xd1=xd+vd*cos_x*dt;
    yd1=yd+vd*cos_y*dt;
    zd1=zd+vd*cos_z*dt;
    %vp=sqrt(abs(xp1-xp)^2+abs(yp1-yp)^2+abs(zp1-zp)^2)/dt;
    dis=sqrt(abs(xd-xp)^2+abs(yd-yp)^2+abs(zd-zp)^2);
    plot3([xd,xd1],[yd,yd1],[zd,zd1],'r.-',[xp,xp1],[yp,yp1],[zp,zp1],'g.-');
    hold on
    xp=xp1;yp=yp1;zp=zp1;
    xd=xd1;yd=yd1;zd=zd1;
    pause(0.05)
end
title(['用时为',num2str(times*dt),'秒'])